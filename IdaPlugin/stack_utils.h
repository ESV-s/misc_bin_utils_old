#pragma once
#include <cstdint>
#include <vector>

/**
* \brief Описание «живого» стека потока во время отладки.
* \details Это РАНТАЙМ-данные: появляются только при запущенном отладчике.
*  План: собирать из карт памяти/сегментов класса STACK для каждого потока.
*/
struct StackRegion
{
	uint32_t thread_id = 0;   ///< \brief Идентификатор потока (отладчика), 0 если неизвестен.
	uint64_t start_ea = 0;   ///< \brief Нижняя граница региона стека (адрес в процессе).
	uint64_t end_ea = 0;   ///< \brief Верхняя граница региона стека (адрес в процессе).
	uint64_t committed = 0;   ///< \brief Оценка закоммиченного объёма, байт (если сможем определить).
};

/**
* \brief Получить список стеков потоков (границы) во время отладки.
* \return true, если удалось собрать данные; false — функция пока не реализована.
*
* \note Заглушка: реализация появится позже (чтение сегментов класса STACK,
*       сопоставление с потоками отладчика). Сейчас возвращает false.
*/
bool GetProcessStackRegions(std::vector<StackRegion> &out);
