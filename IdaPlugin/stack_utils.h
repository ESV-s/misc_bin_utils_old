#pragma once
#include <cstdint>
#include <vector>


/// \brief \n Описание «живого» стека потока во время отладки. \n
/// \details Это РАНТАЙМ-данные: появляются только при запущенном отладчике.
///  План: собирать из карт памяти/сегментов класса STACK для каждого потока.
struct StackRegion
{
	uint32_t thread_id = 0;   ///< \brief \n Идентификатор потока (отладчика), 0 если неизвестен. \n
	uint64_t start_ea = 0;   ///< \brief \n Нижняя граница региона стека (адрес в процессе). \n
	uint64_t end_ea = 0;   ///< \brief \n Верхняя граница региона стека (адрес в процессе). \n
	uint64_t committed = 0;   ///< \brief \n Оценка закоммиченного объёма, байт (если сможем определить). \n
};


/// \brief \n Получить список стеков потоков (границы) во время отладки. \n
/// \return true, если удалось собрать данные; false — функция пока не реализована.
///
/// \note Заглушка: реализация появится позже (чтение сегментов класса STACK,
///       сопоставление с потоками отладчика). Сейчас возвращает false.
bool GetProcessStackRegions(std::vector<StackRegion> &out);
